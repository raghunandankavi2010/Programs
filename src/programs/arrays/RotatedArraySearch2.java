    private boolean search(int[] arr, int target) {
        int low = 0;
        int high = arr.length-1;

        while (low <= high) {
            int mid = (low + high) / 2;
            if (arr[mid] == target) {
                return true;
            }
            // if array in the left is sorted
            if (arr[low] < arr[mid]) {
                if (arr[low] <= target && target < arr[mid]) {
                    high = mid - 1;
                } else {
                    low = mid + 1;
                }
                // array right is sorted
            } else if (arr[low] > arr[mid]) {

                if (arr[mid] < target && target <= arr[high]) {
                    low = mid + 1;
                } else {
                    high = mid - 1;
                }
             // in case low and mid are equal move to right
            } else {
                low++;
            }
        }
        return false;
    }

